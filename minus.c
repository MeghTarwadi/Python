#include <stdio.h>

int main()
{
	int a, b, c;
	printf("\n========================================");
	printf("\n     Subtraction of two variables");
	printf("\n========================================\n");
	printf("\n Enter value of a=");
	scanf("%d", &a);
	printf("\n Enter value of b=");
	scanf("%d", &b);
	c = a + b;
	printf("\n %d + %d = %d \n", a, b, c);
	printf("\n========================================");
	return 0;
}
